package gw.salary;

public class SalaryVO {
	
	private String salary_code;
	private String emp_code;
	private String dept_code;
	private String po_code;
	private String salary_normal;
	private String salary_bonus;
	private String salary_days;
	private String salary_day;
	
	public String getSalary_code() {
		return salary_code;
	}
	public void setSalary_code(String salary_code) {
		this.salary_code = salary_code;
	}
	public String getEmp_code() {
		return emp_code;
	}
	public void setEmp_code(String emp_code) {
		this.emp_code = emp_code;
	}
	public String getDept_code() {
		return dept_code;
	}
	public void setDept_code(String dept_code) {
		this.dept_code = dept_code;
	}
	public String getPo_code() {
		return po_code;
	}
	public void setPo_code(String po_code) {
		this.po_code = po_code;
	}
	public String getSalary_normal() {
		return salary_normal;
	}
	public void setSalary_normal(String salary_normal) {
		this.salary_normal = salary_normal;
	}
	public String getSalary_bonus() {
		return salary_bonus;
	}
	public void setSalary_bonus(String salary_bonus) {
		this.salary_bonus = salary_bonus;
	}
	public String getSalary_days() {
		return salary_days;
	}
	public void setSalary_days(String salary_days) {
		this.salary_days = salary_days;
	}
	public String getSalary_day() {
		return salary_day;
	}
	public void setSalary_day(String salary_day) {
		this.salary_day = salary_day;
	}
}
